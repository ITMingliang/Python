print('====================1.%占位 ======================')
'''
格式化字符串的方式：
1.%作为占位符

2.{}作为占位符
'''

username='张三'
age=18
'''
%为固定符号
1.%s：为字符串
'''
print("我的名字叫：%s,今年%d岁了" %(username,age))  #实际值放在元组内

print('====================2.{}占位 ======================')
print("我的名字叫：{0},今年{1}岁了,我真的叫：{0}".format(username,age))  #实际值放到format方法中,0和1的值对应的是实际值的下标

print('====================3.f-string ======================')
'''版本3.0以上才能使用'''
print(f'我叫：{username}，今年{age}岁了')
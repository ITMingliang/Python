#1.[]进行获取字典中元素
print('=====================1.[]进行获取字典中元素=====================')
score_dic={'张三':100,'李四':99}
print(score_dic['张三'])
#print(score_dic['王二']) #KEY不存在时，报错：KeyError: '王二'

#2.get()进行获取字典中元素
print('=====================2.get()进行获取字典中元素=====================')
print(score_dic.get('张三'))
print(score_dic.get('王二'))     #KEY不存在时，打印出None
print(score_dic.get('王二',99))  #KEY不存在时，提供默认值


'''
区别：
1.[]在获取字典中的元素不存在时候 会抛出异常
2.get()在获取字典中的元素不存在时，会返回None，但是可以进行设置返回值
'''

'''函数定义'''
def fun(a,b,c):
    print('a=', a)
    print('b=', b)
    print('c=', c)

'''函数调用'''
fun(10,20,30)


print('========*lst=========')
lst=[10,20,30]
#fun(lst)  报错
fun(*lst)        #在函数调用时，*将列表的每个元素的都转换为位置实参传入

print('========*dic=========')
dic={'a':10,'b':20,'c':30}
#fun(dic)
fun(*dic) #传入的key值

print('========**dic=========')
fun(**dic)      #在函数调用时，**将字典的每个元素的都转换为位置实参传入
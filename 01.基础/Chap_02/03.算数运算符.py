#基础运算
print(1+1) #加法运算
print(2-1) #减法
print(2*4) #乘法
print(11/2) #除法:5.5
print(11//2) #整除运算:5
print(11%2) #取余运算
print(10**2) #幂运算

#整除
print(9//4)  #2
print(-9//-4) #2
#一正一负，向下取整
print(9//-4) #-3
print(-9//4) #-3

#取余运算
#公式：余数=被除数-除数*商
print(9%-4) #-3  ==> 9-(-4)*(-3)=-3
print(-9%4) #3   ==> -9-4*(-3)=3